require 'rails_helper'

RSpec.describe InvoiceItem, type: :model do
  describe "validations" do
    it { should validate_presence_of :invoice_id }
    it { should validate_presence_of :item_id }
    it { should validate_presence_of :quantity }
    it { should validate_presence_of :unit_price }
    it { should validate_presence_of :status }
  end
  describe "relationships" do
    it { should belong_to :invoice }
    it { should belong_to :item }
  end
  describe "instance methods" do
    describe "#find_invoice_item_applied_discounts_id" do
      before :each do
        @merchant1 = Merchant.create!(name: 'Hair Care')
        @item_1 = Item.create!(name: "Shampoo", description: "This washes your hair", unit_price: 10, merchant_id: @merchant1.id, status: 1)
        @item_8 = Item.create!(name: "Butterfly Clip", description: "This holds up your hair but in a clip", unit_price: 5, merchant_id: @merchant1.id)
        @customer_1 = Customer.create!(first_name: 'Joey', last_name: 'Smith')
        @invoice_1 = Invoice.create!(merchant_id: @merchant1.id, customer_id: @customer_1.id, status: 2, created_at: "2012-03-27 14:54:09")
        @ii_1 = InvoiceItem.create!(invoice_id: @invoice_1.id, item_id: @item_1.id, quantity: 9, unit_price: 10, status: 2)
        @ii_11 = InvoiceItem.create!(invoice_id: @invoice_1.id, item_id: @item_8.id, quantity: 1, unit_price: 10, status: 1)
        @bkd1 = BulkDiscount.create!(merchant_id: @merchant1.id, percent_discount: 10.0, quantity_threshold: 10)
        @bkd2 = BulkDiscount.create!(merchant_id: @merchant1.id, percent_discount: 5.0, quantity_threshold: 5)
      end

      it "finds the id of the bulk discount that was applied to it" do
        expect(@ii_1.find_invoice_item_applied_discounts_id).to eq(@bkd2.id)
      end
    end

    describe "#applied_bulk_discount" do
      before :each do
        @merchant1 = Merchant.create!(name: 'Hair Care')
        @item_1 = Item.create!(name: "Shampoo", description: "This washes your hair", unit_price: 10, merchant_id: @merchant1.id, status: 1)
        @item_8 = Item.create!(name: "Butterfly Clip", description: "This holds up your hair but in a clip", unit_price: 5, merchant_id: @merchant1.id)
        @customer_1 = Customer.create!(first_name: 'Joey', last_name: 'Smith')
        @invoice_1 = Invoice.create!(merchant_id: @merchant1.id, customer_id: @customer_1.id, status: 2, created_at: "2012-03-27 14:54:09")
        @ii_1 = InvoiceItem.create!(invoice_id: @invoice_1.id, item_id: @item_1.id, quantity: 9, unit_price: 10, status: 2)
        @ii_11 = InvoiceItem.create!(invoice_id: @invoice_1.id, item_id: @item_8.id, quantity: 1, unit_price: 10, status: 1)
        @bkd1 = BulkDiscount.create!(merchant_id: @merchant1.id, percent_discount: 10.0, quantity_threshold: 10)
        @bkd2 = BulkDiscount.create!(merchant_id: @merchant1.id, percent_discount: 5.0, quantity_threshold: 5)
      end

      it "finds the bulk discount that was applied" do
        expect(@ii_1.applied_bulk_discount).to eq(@bkd2)
      end
    end
  end
end
